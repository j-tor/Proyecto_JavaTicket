    private void SeeSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeeSearchActionPerformed
        // TODO add your handling code here:
        int eventCode = Integer.parseInt(SeeEvent.getText());

            for (Event evento : eventManager.getEventos()) {
                if (evento.getCode() == eventCode) {
                    String eventData = "Código: " + evento.getCode() + "\n" +
                        "Categoría: " + evento.getCategory() + "\n" +
                        "Título: " + evento.getTitle() + "\n" +
                        "Monto: " + evento.getAmount() + "\n" +
                        "Descripción: " + evento.getDescription() + "\n" +
                        "Día: " + new SimpleDateFormat("yyyy-MM-dd").format(evento.getDay().getTime()) + "\n";

                    if (evento.isCancelled()) {
                        eventData += "Estado: Cancelado" + "\n";
                    } else {
                        eventData += "Estado: Activo" + "\n";
                    }

                    if (evento instanceof EventHerencia) {                        
                        EventHerencia eventoHerencia = (EventHerencia) evento;                        
                        eventData += "Tipo de Evento: " + evento.getEventType() + "\n" +
                            "Equipo 1: " + eventoHerencia.getEquipo1() + "\nEquipo 2: " + eventoHerencia.getEquipo2() + "\n" +
                            "UserCreator:" + currentUser.getUsername();
                    }

                   
                    seearea.setText(eventData);
                    return;
                }
            }
    }//GEN-LAST:event_SeeSearchActionPerformed

