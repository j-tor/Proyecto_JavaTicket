    private void adduser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adduser1ActionPerformed
        // TODO add your handling code here:
    
         Calendar currentDate = Calendar.getInstance();
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    StringBuilder completedEventsDetails = new StringBuilder();
    List<Event> eventosRealizados = new ArrayList<>();
    int deportivosRealizados = 0;
    int religiososRealizados = 0;
    int musicalesRealizados = 0;
    double montoTotalDeportivos = 0.0;
    double montoTotalReligiosos = 0.0;
    double montoTotalMusicales = 0.0;

    // Filtrar eventos realizados y calcular estadísticas
    for (Event evento : eventManager.getEventos()) {
        if (!evento.isCancelled() && evento.isPastEvent()) {
            eventosRealizados.add(evento);
            if (evento.getCategory() == EventCategory.DEPORTIVO) {
                deportivosRealizados++;
                montoTotalDeportivos += evento.getAmount();
            } else if (evento.getCategory() == EventCategory.RELIGIOSO) {
                religiososRealizados++;
                montoTotalReligiosos += evento.getAmount();
            } else if (evento.getCategory() == EventCategory.MUSICAL) {
                musicalesRealizados++;
                montoTotalMusicales += evento.getAmount();
            }
        }
    }

    
    eventosRealizados.sort((e1, e2) -> e2.getDay().compareTo(e1.getDay()));

    // Construir la lista de eventos realizados
    for (Event evento : eventosRealizados) {
        Calendar eventDate = evento.getDay();
        completedEventsDetails.append("Código: ").append(evento.getCode()).append(" - ");
        completedEventsDetails.append("Tipo: ").append(evento.getCategory()).append(" - ");
        completedEventsDetails.append("Título: ").append(evento.getTitle()).append(" - ");
        completedEventsDetails.append("Fecha: ").append(dateFormat.format(eventDate.getTime())).append(" - ");
        completedEventsDetails.append("Monto: ").append(evento.getAmount()).append("\n\n");
        completedEventsDetails.append("\n");
    }

    // Mostrar estadísticas
    completedEventsDetails.append("Eventos deportivos realizados: ").append(deportivosRealizados).append("\n");
    completedEventsDetails.append("Monto total de eventos deportivos: ").append(montoTotalDeportivos).append("\n");
    completedEventsDetails.append("Eventos religiosos realizados: ").append(religiososRealizados).append("\n");
    completedEventsDetails.append("Monto total de eventos religiosos: ").append(montoTotalReligiosos).append("\n");
    completedEventsDetails.append("Eventos musicales realizados: ").append(musicalesRealizados).append("\n");
    completedEventsDetails.append("Monto total de eventos musicales: ").append(montoTotalMusicales).append("\n");

    // Mostrar los detalles en el textarea
    CompletedEventsArea.setText(completedEventsDetails.toString());

    }//GEN-LAST:event_adduser1ActionPerformed

