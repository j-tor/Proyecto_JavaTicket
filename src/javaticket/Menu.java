    private void CanceledEventsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CanceledEventsActionPerformed
        // TODO add your handling code here:
     StringBuilder canceledEventsInfo = new StringBuilder();
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");

    int deportivosCancelados = 0;
    int religiososCancelados = 0;
    int musicalesCancelados = 0;
    double totalMulta = 0.0;

    // Iterar a través de los eventos
    for (Event evento : eventManager.getEventos()) {
        if (evento.isCancelled()) {
            Calendar eventDate = evento.getDay();
            canceledEventsInfo.append("CÓDIGO: ").append(evento.getCode()).append(" - ");
            canceledEventsInfo.append("TIPO: ").append(evento.getCategory()).append(" - ");
            canceledEventsInfo.append("TÍTULO: ").append(evento.getTitle()).append(" - ");
            canceledEventsInfo.append("FECHA: ").append(dateFormat.format(eventDate.getTime())).append(""+ "\n");

            // Calcular la indemnización basada en el tipo de evento
            double indemnizacion = 0.0;
            switch (evento.getCategory()) {
                case DEPORTIVO:
                    indemnizacion = evento.getAmount()* 0.5; 
                    deportivosCancelados++;
                    break;
                case RELIGIOSO:
                    indemnizacion = evento.getAmount()* 0; 
                    religiososCancelados++;
                    break;
                case MUSICAL:
                    indemnizacion = evento.getAmount()* 0.50; 
                    musicalesCancelados++;
                    break;
            }
            totalMulta += indemnizacion;

           
        }
        
    }
    
    Canceledevents.setText(canceledEventsInfo.toString());
    String estadisticas = "Eventos DEPORTIVOS cancelados: " + deportivosCancelados + "\n";
    estadisticas += "Eventos RELIGIOSOS cancelados: " + religiososCancelados + "\n";
    estadisticas += "Eventos MUSICALES cancelados: " + musicalesCancelados + "\n";
    estadisticas += "Total de multa por indemnización: " + totalMulta;
    Canceledevents.setText(canceledEventsInfo.toString()+"\n\n"+estadisticas);
    }//GEN-LAST:event_CanceledEventsActionPerformed

